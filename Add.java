
public class Add {
	public static void main(String[] args) {
		int x =10;
        int y =5;
        int z;          
        z = x+y; // + addition
        System.out.println(z);
        z = x-y; // - subtraction
        System.out.println(z);
        z =x*y; // * Multiplication
        System.out.println(z);
        z= x/y; // / Division
        System.out.println(z);
        z= x%y; // % Modulus
        System.out.println(z);
	}

}
